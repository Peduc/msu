    Yak(1,1)=(2*(U(2) - U(1)))/...
             (Eps*((4*U(1)^2*(U(2) - U(1))^2)/Eps^2 + 1)^(1/2)) - ...
             (2*U(1))/(Eps*((4*U(1)^2*(U(2) - U(1))^2)/Eps^2 + 1)^(1/2)) - ...
             (U(1)*((8*U(1)*(U(2) - U(1))^2)/Eps^2 - ...
             (4*U(1)^2*(2*U(2) - 2*U(1)))/Eps^2)*(U(2) - U(1)))/...
             (Eps*((4*U(1)^2*(U(2) - U(1))^2)/Eps^2 + 1)^(3/2));
    Yak(1,2)=(2*U(1))/(Eps*((4*U(1)^2*(U(2) - U(1))^2)/Eps^2 + 1)^(1/2))-...
             (4*U(1)^3*(U(2) - U(1))*(2*U(2) - 2*U(1)))/...
             (Eps^3*((4*U(1)^2*(U(2) - U(1))^2)/Eps^2 + 1)^(3/2));   
    Yak(2,1)=-((8*U(1)*(U(2) - U(1))^2)/Eps^2 - (4*U(1)^2*(2*U(2) - ...
             2*U(1)))/Eps^2)/(2*Eps*((4*U(1)^2*(U(2) - ...
             U(1))^2)/Eps^2 + 1)^(3/2));
    Yak(2,2)=-(2*U(1)^2*(2*U(2) - 2*U(1)))/...
             (Eps^3*((4*U(1)^2*(U(2) - U(1))^2)/Eps^2 + 1)^(3/2));    
end

